def test_agentshore_identity_reconfigure_invokes_wizard_without_db_reset(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    """``agentshore identity --reconfigure`` re-runs the wizard (writes new
    bindings) and leaves an existing ``.agentshore/agentshore.db`` untouched."""
    from click.testing import CliRunner

    from agentshore.cli import main

    # Pre-existing agentshore.yaml and DB.
    cfg_path = tmp_path / "agentshore.yaml"
    cfg_path.write_text(_BASE_YAML, encoding="utf-8")
    agentshore_dir = tmp_path / ".agentshore"
    agentshore_dir.mkdir()
    db_path = agentshore_dir / "agentshore.db"
    db_path.write_bytes(b"sentinel-do-not-touch")
    db_mtime_before = db_path.stat().st_mtime_ns

    captured: dict[str, object] = {}

    def fake_run_wizard(
        config_path: Path,
        agent_keys: object,
        *,
        force_run: bool = False,
        defaults: dict[str, str] | None = None,
        existing_identities: object = None,
        repo_name_with_owner: str | None = None,
    ) -> None:
        captured["config_path"] = config_path
        captured["agent_keys"] = list(agent_keys)
        captured["force_run"] = force_run
        captured["defaults"] = defaults
        captured["repo_name_with_owner"] = repo_name_with_owner

    monkeypatch.setattr("agentshore.cli_identity.run_identity_wizard", fake_run_wizard)
    monkeypatch.setattr(
        "agentshore.cli._detect_gh_remote",
        lambda _project: {"nameWithOwner": "example-user/example-repo"},
    )

    runner = CliRunner()
    result = runner.invoke(main, ["identity", "--project", str(tmp_path), "--reconfigure"])
    assert result.exit_code == 0, result.output
    assert captured["config_path"] == cfg_path
    assert captured["agent_keys"] == ["claude_code", "codex"]
    assert captured["force_run"] is True
    # DB file untouched.
    assert captured["repo_name_with_owner"] == "example-user/example-repo"
    assert db_path.read_bytes() == b"sentinel-do-not-touch"
    assert db_path.stat().st_mtime_ns == db_mtime_before


