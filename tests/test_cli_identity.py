_GH_STATUS_TEXT = """\
github.com
  ✓ Logged in to github.com account example-user (active)
    - Active account: true
    - Git operations protocol: ssh
    - Token: gho_*****
    - Token scopes: 'gist', 'read:org', 'repo'
  ✓ Logged in to github.com account unseriousAI
    - Active account: false
    - Git operations protocol: ssh
    - Token: gho_*****
"""


def test_parse_gh_auth_status_two_accounts() -> None:
    accounts = parse_gh_auth_status(_GH_STATUS_TEXT)
    logins = [a.login for a in accounts]
    assert logins == ["example-user", "unseriousAI"]
    assert accounts[0].active is True
    assert accounts[1].active is False


def test_parse_gh_auth_status_empty() -> None:
    assert parse_gh_auth_status("") == []


def test_parse_gh_auth_status_ignores_other_hosts() -> None:
    text = """\
gitlab.com
  ✓ Logged in to gitlab.com account someone (active)
"""
    assert parse_gh_auth_status(text) == []


